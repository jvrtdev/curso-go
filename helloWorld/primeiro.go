package main

import("fmt")

func main() {
	fmt.Println("Hello World!")
}

//O package main define qual é o package principal do programa. Acompanhando ele, precisamos implementar a função main, que é o ponto de entrada do programa. O nome do package é sempre declarado na primeira linha de cada arquivo Go.
//fmt É um pacote do GO que é utilizado para imprimir informações na tela, dentro desse pacote existem outras funções, a mais usada é o Println(  ) para imprimir informações na tela